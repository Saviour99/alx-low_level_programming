#include <stdio.h>

/**
 *add - add two numbers
 *@a: first number
 *@b: second number
 *
 * Return: addition
 */

int add(int a, int b)
{
	return a + b;
}

/**
 *sub - subtract two numbers
 *@a: first number
 *@b: second number
 *
 * Return: subtration
 */

int sub(int a, int b)
{
        return a - b;
}

/**
 *mul - multiply two numbers
 *@a: first number
 *@b: second number
 *
 * Return: multiplication
 */

int mul(int a, int b)
{
        return a * b;
}

/**
 *div - divides two numbers
 *@a: first number
 *@b: second number
 *
 * Return: division
 */

int div(int a, int b)
{
        return a / b;
}

/**
 *mod - moduloof two numbers
 *@a: first number
 *@b: second number
 *
 * Return: modulo
 */

int mod(int a, int b)
{
        return a % b;
}
